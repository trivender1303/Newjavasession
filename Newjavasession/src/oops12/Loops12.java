package oops12;

public class Loops12 {

	public static void main(String[] args) {

		// while loop
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i = i + 1;
		}

		System.out.println("**********");

		// for loop

		for (int j = 1; j <= 10; j++) {
			System.out.println(j);
		}
		System.out.println("************");
		//writing 10 to 1
		for(int g=10;g>=0;g--){
			
			System.out.println(g);
		}

	}

}
